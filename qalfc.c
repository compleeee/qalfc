float fsum(float a, float b)
{
    return a + b;
}

float fsub(float a, float b)
{
    return a - b;
}

float fmol(float a, float b)
{
    return a * b;
}

float fdiv(float a, float b)
{
    return a / b;
}

int sum(int a, int b)
{
    return a + b;
} 

int sub(int a, int b)
{
    return a - b;
}

int mol(int a, int b)
{
    return a * b;
}

int div(int a, int b)
{
    return a / b;
}

int rest(int a, int b)
{
    return a % b;
}
